ll dp(ll j, char u){
    if(j == n) return 0;
    if(memo[j][mp[u]] != -1) return memo[j][mp[u]];

    if(u == 'R'){
        ll aux = 0;
        if(s[j] == 'S') aux++;
        else if(s[j] == 'P') return -INF;
        memo[j][mp[u]] = max(dp(j+1, 'P'), dp(j+1, 'S')) + aux;
    }else if(u == 'P'){
        ll aux = 0;
        if(s[j] == 'R') aux++;
        else if(s[j] == 'S') return -INF;
        memo[j][mp[u]] = max(dp(j+1, 'R'), dp(j+1, 'S')) + aux;
    }else if(u == 'S'){
        ll aux = 0;
        if(s[j] == 'P') aux++;
        else if(s[j] == 'R') return -INF;
        memo[j][mp[u]] = max(dp(j+1, 'R'), dp(j+1, 'P')) + aux;
    }

    return memo[j][mp[u]];
}

